#include "libft.h"

int	ft_atoi(const char *str)
{
	int					i;
	unsigned long long	result;
	int					signe;

	result = 0;
	signe = 1;
	i = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signe = -1;
		i++;
	}
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		if (result > 9223372036854775807 && signe == 1)
			return (-1);
		if (result > 9223372036854775807 && signe == -1)
			return (0);
		i++;
	}
	return (result * signe);
}
